char tipo_grrr(){
	char scelta;
	printf("Che tipo di GRRR vuoi?\n    a) Semplice\n    b) Bidimensionale\n    c) Piramide\n    d) Disegno ASCII\nInserisci: ");
	scanf(" %c", &scelta);
	return scelta;
};

int dimensione_grrr(char tipo){
	int dim;
	do {
		printf("Di che dimensione vuoi");
		if (tipo == 'b') {
			printf(" il GRRR bidimensionale? ");
			scanf("%i", &dim);
		} else {
			printf(" la piramide di GRRR? ");
			scanf("%i", &dim);
		}
	} while(dim < 1);
	return dim;
};

